int main()
{
    int x;
    float x_sin,x_cos,x_sqrt,x_exp,x_log;

    x = 0;
    x_sin = sin(x);
    x_cos = cos(x);
    x_sqrt = sqrt(x);
    x_exp = exp(x);
    x_log = log(x);

    printf("Sin : %f\n",x_sin);
    printf("Cos : %f\n",x_cos);
    printf("Sqrt : %f\n",x_sqrt);
    printf("Exp : %f\n",x_exp);
    printf("Log : %f\n",x_log);
}